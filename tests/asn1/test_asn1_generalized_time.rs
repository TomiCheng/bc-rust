use bc_rust::asn1;
use bc_rust::util::encoders::hex;

#[test]
fn test_01() {
    let inputs = [
        "20020122122220",
        "20020122122220Z",
        "20020122122220-1000",
        "20020122122220+00",
        "20020122122220.1",
        "20020122122220.1Z",
        "20020122122220.1-1000",
        "20020122122220.1+00",
        "20020122122220.01",
        "20020122122220.01Z",
        "20020122122220.01-1000",
        "20020122122220.01+00",
        "20020122122220.001",
        "20020122122220.001Z",
        "20020122122220.001-1000",
        "20020122122220.001+00",
        "20020122122220.0001",
        "20020122122220.0001Z",
        "20020122122220.0001-1000",
        "20020122122220.0001+00",
        "20020122122220.0001+1000"
    ];

    for index in 0..inputs.len() {
        let input = inputs[index];
        assert!(asn1::Asn1GeneralizedTime::with_str(input).is_ok());
    }
}

